<p>2. Obligations des parties </p>
Chaque partie s engage, d’une part, à ne pas divulguer, ni à communiquer à quiconque tout ou partie des Informations confidentielles et, d’autre part, à prendre toute disposition pour que cette confidentialité soit préservée.
Chaque partie, s engage à ne faire aucun usage des informations confidentielles dans un but autre que celui des Relations professionnelles.
Par les présentes, chacune des Parties s’engage irrévocablement, et se porte fort de ce que ses Affiliés et Représentants s’engagent, sans condition, limitation ou restriction, à :

n'utiliser les Informations Confidentielles que pour son propre compte et aux seules fins d’étudier les conditions de réalisation du Projet, et notamment s’abstenir d'utiliser les Informations Confidentielles d'une quelconque manière qui pourraient porter atteinte à l’autre Partie ou ses Affiliés, à leurs activités industrielles, commerciales et financières, ou de façon générale qui pourrait porter atteinte à leur valorisation;

assurer la plus stricte confidentialité des Informations Confidentielles et à ne pas les reproduire, à ne pas les divulguer ou communiquer, en tout ou partie, de quelque manière que ce soit, à des personnes ou entités autres que celles visées au paragraphe c) ci-dessous. 
Dans l’hypothese où l’une ou l’autre des Parties ou un de ses Représentants serait dans l’obligation légale ou r&eacuteglementaire, en vertu d'une décision de justice ou de toute loi ou réglementation applicable, de divulguer des Informations Confidentielles à des autorités administratives, arbitrales ou judiciaires, elle s’engage à fournir dans les plus brefs délais à l’autre Partie (dans la mesure où cela est légalement possible) la notification préalable et écrite d’une telle obligation afin qu’elle puisse, le cas échéant, prendre toute mesure ou action de protection, et qu’en toutes circonstances soit possible une consultation préalable avec l’autre Partie sur l’étendue et le calendrier de la divulgation envisagée ;

